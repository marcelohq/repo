package com.media.aluno;

public class Aluno {

	private String nome;
	private Double media;
	
	
	public void setNome(String nome) {
		
		this.nome = nome;
	}
	public String getNome() {	
		return this.nome;
	}
	
	public void setMedia(Double media) {
		this.media = media;
	}
	public Double getMedia() {
		
		return this.media;
	}
}
